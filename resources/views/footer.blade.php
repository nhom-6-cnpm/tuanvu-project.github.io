<script>
function markAsRead(id) {
    $.ajax({
        type: 'POST',
        url: '/admin/contacts/mark-as-read/' + id,
        data: {
            _token: '{{ csrf_token() }}'
        },
        success: function(result) {
            if (result.error === false) {
                alert(result.message);
                location.reload();
            } else {
                alert('Có lỗi xảy ra, vui lòng thử lại');
            }
        }
    });
}
</script>